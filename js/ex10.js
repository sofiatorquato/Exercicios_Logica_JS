let Dia = Number(prompt("Informe um número de 1 a 7: "));

switch (Dia) {
  case 1:
    alert("1 - Domingo");
    break;

  case 2:
    alert("2 - Segunda");
    break;
  case 3:
    alert("3 - Terça");
    break;
  case 4:
    alert("4 - Quarta");
    break;
  case 5:
    alert("5 - Quinta");
    break;
  case 6:
    alert("6 - Sexta");
    break;
  case 7:
    alert("7 - Sábado");
    break;
}
